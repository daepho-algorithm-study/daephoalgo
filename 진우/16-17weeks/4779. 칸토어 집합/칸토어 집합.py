import sys

# ---------------------------
# 칸토어 집합을 만드는 재귀 함수
# s : 전체 문자열 (리스트로 전달 → 원본 수정 가능)
# l : 현재 구간의 시작 인덱스
# r : 현재 구간의 끝 인덱스 (미포함)
# ---------------------------
def cantor(s, l, r):

    # [기저 조건]
    # 구간 길이가 1이면 더 이상 나눌 수 없으므로 종료
    if r - l < 2:
        return

    # 현재 구간 길이의 1/3 계산
    length = (r - l) // 3

    # ---------------------------
    # 가운데 1/3 구간을 공백으로 변경
    # 구간: [l + length, l + 2*length)
    # ---------------------------
    for i in range(l + length, l + 2 * length):
        s[i] = ' '

    # ---------------------------
    # 왼쪽 1/3 구간에 대해 재귀 호출
    # ---------------------------
    cantor(s, l, l + length)

    # ---------------------------
    # 오른쪽 1/3 구간에 대해 재귀 호출
    # ---------------------------
    cantor(s, l + 2 * length, r)

while True:
    try:
        N = int(input())

        # 3^N 길이 계산
        size = 1
        for _ in range(N):
            size *= 3

        # '-'로 초기화된 문자 리스트 생성
        s = ['-'] * size

        # 칸토어 집합 생성
        cantor(s, 0, size)

        # 리스트 → 문자열로 변환 후 출력
        print(''.join(s))
    except EOFError:
        break;