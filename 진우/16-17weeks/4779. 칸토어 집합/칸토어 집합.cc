#include <iostream>
#include <string>

using namespace std;

// ---------------------------
// 칸토어 집합을 만드는 재귀 함수
// s : 전체 문자열 (참조로 전달 → 원본 수정)
// l : 현재 구간의 시작 인덱스
// r : 현재 구간의 끝 인덱스 (미포함)
// ---------------------------
void cantor(string& s, int l, int r) {

    // [기저 조건]
    // 구간 길이가 1이면 더 이상 나눌 수 없으므로 종료
    if (r - l < 2) return;

    // 현재 구간 길이의 1/3 계산
    int len = (r - l) / 3;

    // ---------------------------
    // 가운데 1/3 구간을 공백으로 변경
    // 구간: [l + len, l + 2*len)
    // ---------------------------
    for (int i = l + len; i < l + 2 * len; i++) {
        s[i] = ' ';
    }

    // ---------------------------
    // 왼쪽 1/3 구간에 대해 재귀 호출
    // ---------------------------
    cantor(s, l, l + len);

    // ---------------------------
    // 오른쪽 1/3 구간에 대해 재귀 호출
    // ---------------------------
    cantor(s, l + 2 * len, r);
}

int main() {
    int N;

    // 입력이 끝날 때까지 여러 줄 처리
    while (cin >> N) {

        // 3^N 길이 계산
        int size = 1;
        for (int i = 0; i < N; i++)
            size *= 3;

        // 길이가 size인 문자열을 '-'로 초기화
        string s(size, '-');

        // 칸토어 집합 생성 (전체 구간 0 ~ size)
        cantor(s, 0, size);

        // 결과 출력
        cout << s << '\n';
    }

    return 0;
}
