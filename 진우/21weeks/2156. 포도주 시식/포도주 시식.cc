#include <iostream>
#include <vector>
#include <algorithm>

using namespace std;

int dp[10001];   // dp[i] : i번째 잔까지 고려했을 때 마실 수 있는 포도주의 최대 양

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(nullptr);

    int n;
    cin >> n;    // 포도주 잔의 개수

    vector<int> arr(n + 1);
    arr[0] = 0;  // 인덱스 맞추기용 (실제 사용하지 않음)

    // 각 포도주 잔에 들어 있는 포도주의 양 입력
    for (int i = 1; i <= n; i++) {
        cin >> arr[i];
    }

    /* ===============================
       초기값 설정
       =============================== */
    dp[0] = 0;              // 잔이 0개일 때는 마실 수 있는 포도주가 없음
    dp[1] = arr[1];         // 잔이 1개일 때는 첫 잔을 마시는 것이 최대
    dp[2] = arr[1] + arr[2]; // 잔이 2개일 때는 두 잔 모두 마실 수 있음

    /* ===============================
       DP 계산 (3번째 잔부터)
       =============================== */
    for (int i = 3; i <= n; i++) {
        // 경우 1: i-1번째 잔과 i번째 잔을 마시는 경우
        //         → i-3번째 잔까지의 최대값 + arr[i-1] + arr[i]
        //
        // 경우 2: i번째 잔만 마시는 경우
        //         → i-2번째 잔까지의 최대값 + arr[i]
        //
        // 경우 3: i번째 잔을 마시지 않는 경우
        //         → i-1번째 잔까지의 최대값 유지
        dp[i] = max({
            dp[i - 3] + arr[i - 1] + arr[i],
            dp[i - 2] + arr[i],
            dp[i - 1]
        });
    }

    /* ===============================
       최종 정답 출력
       =============================== */
    // dp[n]에는 n번째 잔까지 고려했을 때의 최대 포도주 양이 저장됨
    cout << dp[n] << "\n";

    return 0;
}
