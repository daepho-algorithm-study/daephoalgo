import sys
input = sys.stdin.readline

n = int(input())  # 포도주 잔의 개수

# 포도주 양 배열 (인덱스 맞추기 위해 0번 사용 안 함)
arr = [0] * (n + 1)
for i in range(1, n + 1):
    arr[i] = int(input())

# dp[i] : i번째 잔까지 고려했을 때 마실 수 있는 최대 포도주 양
dp = [0] * (n + 1)

# ===============================
# 초기값 설정
# ===============================
if n >= 1:
    dp[1] = arr[1]              # 잔이 1개일 때
if n >= 2:
    dp[2] = arr[1] + arr[2]     # 잔이 2개일 때

# ===============================
# DP 계산 (3번째 잔부터)
# ===============================
for i in range(3, n + 1):
    dp[i] = max(
        dp[i - 3] + arr[i - 1] + arr[i],  # i-1, i 마시는 경우
        dp[i - 2] + arr[i],               # i만 마시는 경우
        dp[i - 1]                         # i 안 마시는 경우
    )

# ===============================
# 최종 정답 출력
# ===============================
print(dp[n])
