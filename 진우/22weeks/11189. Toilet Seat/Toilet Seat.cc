#include <iostream>
#include <string>
#include <vector>

using namespace std;

int main() {
    string s;
    cin >> s; // 입력 문자열: 'U'와 'D'로 구성, 첫 문자는 초기 상태

    // ==============================
    // 항상 U로 사용 정책 계산
    // ==============================
    int up = 0;
    char cur1 = s[0]; // 현재 변기 상태 추적
    for (int i = 1; i < s.size(); i++) {
        // 이전 상태와 다르면 조작
        if (s[i] != cur1) {
            up++;       // 조작 1회
            cur1 = s[i]; // 상태 갱신
        }

        // 항상 U 정책: 현재 변기가 U가 아니면 올림
        if (cur1 != 'U') {
            up++;       // 조작 1회
            cur1 = 'U'; // 변기 상태를 U로 맞춤
        }
    }

    // ==============================
    // 항상 D로 사용 정책 계산
    // ==============================
    int down = 0;
    char cur2 = s[0]; // 현재 변기 상태 추적
    for (int i = 1; i < s.size(); i++) {
        // 이전 상태와 다르면 조작
        if (s[i] != cur2) {
            down++;       // 조작 1회
            cur2 = s[i];  // 상태 갱신
        }

        // 항상 D 정책: 현재 변기가 D가 아니면 내림
        if (cur2 != 'D') {
            down++;       // 조작 1회
            cur2 = 'D';   // 변기 상태를 D로 맞춤
        }
    }

    // ==============================
    // 현재 상태 유지 정책 계산
    // ==============================
    int keep = 0;
    char cur3 = s[0]; // 현재 변기 상태 추적
    for (int i = 1; i < s.size(); i++) {
        // 이전 사람과 상태가 다르면 조작
        if (s[i] != cur3) {
            keep++;       // 조작 1회
            cur3 = s[i];  // 상태 갱신
        }
    }

    // ==============================
    // 결과 출력
    // ==============================
    cout << up << "\n";    // 항상 U 정책 조작 횟수
    cout << down << "\n";  // 항상 D 정책 조작 횟수
    cout << keep << "\n";  // 현재 상태 유지 정책 조작 횟수

    return 0;
}
