# 입력
s = input().strip()  # 'U'와 'D'로 구성된 문자열

# ==============================
# 항상 U 정책 계산
# ==============================
up = 0
cur1 = s[0]  # 현재 변기 상태 추적
for i in range(1, len(s)):
    # 이전 상태와 다르면 조작
    if s[i] != cur1:
        up += 1
        cur1 = s[i]  # 상태 갱신

    # 항상 U 정책: 현재 변기가 U가 아니면 올림
    if cur1 != 'U':
        up += 1
        cur1 = 'U'  # 변기 상태를 U로 맞춤

# ==============================
# 항상 D 정책 계산
# ==============================
down = 0
cur2 = s[0]  # 현재 변기 상태 추적
for i in range(1, len(s)):
    # 이전 상태와 다르면 조작
    if s[i] != cur2:
        down += 1
        cur2 = s[i]  # 상태 갱신

    # 항상 D 정책: 현재 변기가 D가 아니면 내림
    if cur2 != 'D':
        down += 1
        cur2 = 'D'  # 변기 상태를 D로 맞춤

# ==============================
# 현재 상태 유지 정책 계산
# ==============================
keep = 0
cur3 = s[0]  # 현재 변기 상태 추적
for i in range(1, len(s)):
    # 이전 상태와 다르면 조작
    if s[i] != cur3:
        keep += 1
        cur3 = s[i]  # 상태 갱신

# ==============================
# 결과 출력
# ==============================
print(up)     # 항상 U 정책 조작 횟수
print(down)   # 항상 D 정책 조작 횟수
print(keep)   # 현재 상태 유지 정책 조작 횟수
