# 사용자로부터 정수 입력 받기
N = int(input())

# 소인수를 저장할 리스트
arr = []

# 나눌 수를 2부터 시작 (가장 작은 소수)
x = 2

# N이 1보다 큰 동안 계속 나누기
while N > 1:
    # 현재 x가 N을 나눌 수 있는 경우 (즉, x가 N의 약수일 경우)
    if N % x == 0:
        arr.append(x)   # 소인수 목록에 x 추가
        N //= x         # N을 x로 나눈 몫으로 갱신 (정수 나눗셈)
    else:
        x += 1          # 나눌 수를 1 증가시켜 다음 후보 검사

# 소인수 출력
for i in arr:
    print(i)
