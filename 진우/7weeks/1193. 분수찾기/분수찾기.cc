#include <iostream>
using namespace std;

int main() {
    int X;
    cin >> X;  // 찾고자 하는 X번째 분수의 위치 입력

    int line = 2;  // 현재 대각선(line)의 번호 (분수 그룹을 나타냄)
    int cnt = 2;   // 현재 분수의 순서 (몇 번째인지 세는 용도)
    int i = 1;     // 분자 (초기값 1/1에서 시작)
    int j = 1;     // 분모

    // X번째 분수에 도달할 때까지 반복
    while (cnt <= X) {
        // 짝수 번째 대각선 (오른쪽 위 방향)
        if (line % 2 == 0) {
            j++;   // 분모 증가
            i--;   // 분자 감소
            // 분자가 0이 되면 다음 대각선으로 이동
            if (i == 0) {
                line += 1;  // 다음 대각선으로
                i = 1;      // 분자 초기화
            }
        }
        // 홀수 번째 대각선 (왼쪽 아래 방향)
        else {
            i++;   // 분자 증가
            j--;   // 분모 감소
            // 분모가 0이 되면 다음 대각선으로 이동
            if (j == 0) {
                line += 1;  // 다음 대각선으로
                j = 1;      // 분모 초기화
            }
        }
        cnt += 1;  // 다음 분수로 이동
    }

    // X번째 분수 출력
    cout << i << "/" << j << "\n";
    return 0;
}
