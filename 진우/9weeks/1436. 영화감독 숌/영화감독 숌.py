# N번째 '666' 시리즈를 찾는 프로그램

# 사용자로부터 N 입력받기
N = int(input())

count = 0   # 지금까지 발견한 "666"이 포함된 숫자의 개수
num = 666   # 첫 "666" 시리즈부터 시작

# 무한 반복문 — N번째 666이 나올 때까지 계속 반복
while True:
    # 숫자를 문자열로 변환하여 "666"이 포함되어 있는지 확인
    if '666' in str(num):
        count += 1  # 666 시리즈 발견 시 카운트 증가

    # 만약 지금까지 찾은 개수가 N과 같다면, N번째 666을 찾은 것임
    if count == N:
        print(num)  # 해당 숫자 출력
        break       # 반복문 종료

    # 다음 숫자로 이동
    num += 1
